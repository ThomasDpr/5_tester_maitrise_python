def sum(a, b):
    """
    Calcule la somme de deux nombres.

    Args:
        a (int | float): le premier nombre
        b (int | float): le second nombre

    Returns:
        int | float: la somme des deux nombres
    """

    return a + b


def subtraction(a, b):
    """
    Calcule la différence de deux nombres.

    Args:
        a (int | float): le premier nombre
        b (int | float): le second nombre

    Returns:
        int | float: la différence des deux nombres
    """

    return a - b
